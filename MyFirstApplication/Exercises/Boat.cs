
namespace MyFirstApplication;

internal class Boat
{
    public int _boatType;
    public int _travels;
    public int name;

    public int BoatType
    {
        get => _boatType;
        set => _boatType = value;
    }

    public int Travels
    {
        get => _travels;
        set => _travels = value;
    }

    public int Name
    {
        get => name;
        set => name = value;
    }


}// end class
